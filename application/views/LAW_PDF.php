                <h2>方法</h2>
                <div class="row">
                  <div class="col-md-8 col-md-offset-2">
                    <div class="thumbnail">
                      <img src="\0MS\images\Support-PDF\003.jpg" alt="...">
                      <div class="caption">
                        <h3>步驟1.</h3>
                        <p>點擊<a href="https://chrome.google.com/webstore/detail/pdf-viewer/oemmndcbldboiebfnladdacbdfmadadm/related" target="_blank">此超連結</a>，或進入Chrome應用商店搜尋"PDF Viewer"，然後點選"加到Chrome"(如照片中紅圈處)</p>
                      </div>
                    </div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-md-8 col-md-offset-2">
                    <div class="thumbnail">
                      <img src="\0MS\images\Support-PDF\004.jpg" alt="...">
                      <div class="caption">
                        <h3>步驟2.</h3>
                        <p>在彈出的確認視窗中，選擇"新增擴充功能"(如照片中紅圈處)</p>
                      </div>
                    </div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-md-8 col-md-offset-2">
                    <div class="thumbnail">
                      <img src="\0MS\images\Support-PDF\005.jpg" alt="...">
                      <div class="caption">
                        <h3>步驟3.</h3>
                        <p>Google Chrome瀏覽器的工具列上會出現一個新的功能按鈕，但未啟動呈現灰色，滑鼠指過去不動會顯示"show PDF URL"(如照片中紅圈處)</p>
                      </div>
                    </div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-md-8 col-md-offset-2">
                    <div class="thumbnail">
                      <img src="\0MS\images\Support-PDF\006.jpg" alt="...">
                      <div class="caption">
                        <h3>步驟4.</h3>
                        <p>對著此按鈕點擊滑鼠右鍵，然後點選"選項"(如照片中紅圈處)</p>
                      </div>
                    </div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-md-8 col-md-offset-2">
                    <div class="thumbnail">
                      <img src="\0MS\images\Support-PDF\007.jpg" alt="...">
                      <div class="caption">
                        <h3>步驟5.</h3>
                        <p>在彈出的確認視窗中，在"Default zoom level"這個項目，選擇"Full Width"(如照片中紅箭頭處)</p>
                      </div>
                    </div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-md-8 col-md-offset-2">
                    <div class="thumbnail">
                      <img src="\0MS\images\Support-PDF\008.jpg" alt="...">
                      <div class="caption">
                        <h3>步驟6.</h3>
                        <p>確認選擇正確後，再點選右上角的小圓叉叉，以關閉設定(如照片中紅箭頭處)</p>
                      </div>
                    </div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-md-8 col-md-offset-2">
                    <div class="thumbnail">
                      <img src="\0MS\images\Support-PDF\009.jpg" alt="...">
                      <div class="caption">
                        <h3>步驟7.</h3>
                        <p>回到本系統中，於顯示功能中，再次點選"外部開啟"(如照片中紅箭頭處)</p>
                      </div>
                    </div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-md-8 col-md-offset-2">
                    <div class="thumbnail">
                      <img src="\0MS\images\Support-PDF\010.jpg" alt="...">
                      <div class="caption">
                        <h3>步驟8.</h3>
                        <p>PDF檔會在新的分頁中開啟，並且以頁面寬度(如照片中紅箭頭處)的預設方式呈現</p>
                      </div>
                    </div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-md-8 col-md-offset-2">
                    <div class="thumbnail">
                      <img src="\0MS\images\Support-PDF\011.jpg" alt="...">
                      <div class="caption">
                        <h3>步驟9.</h3>
                        <p>有些PDF檔掃描進來時，可能會有方向不合的問題。<br>請別擔心!只要先點選畫面右上角的＞＞按鈕(如照片中紅圈處)</p>
                      </div>
                    </div>
                  </div>
                </div>
                <div class="row">
                  <div class="col-md-8 col-md-offset-2">
                    <div class="thumbnail">
                      <img src="\0MS\images\Support-PDF\012.jpg" alt="...">
                      <div class="caption">
                        <h3>步驟10.</h3>
                        <p>再點選順時鐘旋轉(如照片中紅箭頭)</p>
                      </div>
                    </div>
                  </div>
                </div>
